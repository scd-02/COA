;OBJ3 - MOVE A BLOCK OF 16-BIT DATA FROM ONE LOCATION TO OTHER 
;INTER SEGMENT DATA TRANSFER

MOV AX, 1000H
MOV BX, 3000H
MOV SI, 2000H
MOV DI, 2000H
MOV DS, AX

MOV CL, [SI]

L1: INC SI
    INC SI

    MOV DS, AX
    MOV DX, [SI]

    MOV DS, BX
    INC DI
    INC DI

    MOV [DI], DX
    DEC CL

    JNZ L1
HLT