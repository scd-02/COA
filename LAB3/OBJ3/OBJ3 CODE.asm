;OBJ3 - MOVE A BLOCK OF 16-BIT DATA FROM ONE LOCATION TO OTHER 

;INTRA SEGMENT DATA TRANSFER

MOV AX, 1000H
MOV DS, AX
MOV SI, 2000H
MOV DI, 2012H

MOV CL, [SI]

L1: INC SI
    INC SI

    MOV AX, [SI]
    MOV [DI], AX

    INC DI
    INC DI

    DEC CL
    JNZ L1
HLT